#include <unistd.h>

void	ft_print_comb(void)
{
	char	c;
	char	d;
	char	u;

	c = '0';
	while (c <= '7')
	{
		d = c + 1;
		while (d <= '8')
		{
			u = d + 1;
			while (u <= '9')
			{
				if (u != '2')
					write(1, ", ", 2);
				write(1, &c, 1);
				write(1, &d, 1);
				write(1, &u, 1);
				u = u + 1;
			}
			d = d + 1;
		}
		c = c + 1;
	}
}
