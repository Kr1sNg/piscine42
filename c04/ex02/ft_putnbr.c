#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	a;
	char	c;

	a = nb;
	if (a < 0)
	{
		write(1, "-", 1);
		a = a * (-1);
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else
	{
		c = a + '0';
		write(1, &c, 1);
	}
}
